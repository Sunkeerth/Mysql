-- What is a Subquery in SQL?
-- A Subquery, also called an inner query or nested query, is a query within another query.
--  It's used to return data that will be used by the main query (outer query).

 --                                 Syntax:
-- SELECT column1, column2, ... FROM table WHERE column_name OPERATOR (
--     SELECT column_name FROM another_table WHERE condition  );

-- Types of subquery :
-- 1.single row subquery .
-- 2.Multi row subquery.
-- 3. correlated subquery.
-- 4. non- correlated subquery.
-- 5. Nested subquery.